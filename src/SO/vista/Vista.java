    public void run() throws IOException {
        String cmd = jTextField1.getText();
        String[] splits = cmd.split(" ");
        File directorio;
        switch (splits[0]) {
            case "crdir":
                directorio = new File(defaultdir, splits[1]);
                directorio.mkdir();
                imprime("Creado " + splits[1]);
                break;
            case "crfile":
                if (splits.length == 3) {
                    directorio = new File(defaultdir, splits[2]);
                    if (!directorio.exists()) {
                        directorio.mkdir();
                        imprime("Creado " + splits[2]);
                    }
                    try {
                        File archivo;
                        archivo = new File(directorio, splits[1]);
                        archivo.createNewFile();
                        imprime("Creado " + splits[1] + " en " + splits[2]);
                    } catch (IOException ex) {
                        Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (splits.length == 2) {
                    try {
                        File archivo;
                        archivo = new File(defaultdir, splits[1]);
                        archivo.createNewFile();
                        imprime("Creado " + splits[1]);
                    } catch (IOException ex) {
                        Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
                break;

            case "dir":
                if (splits.length == 1) {
                    File dir = new File(defaultdir);
                    String[] ficheros = dir.list();
                    if (ficheros == null) {
                        System.out.println("Nel prra, nel");
                    } else {
                        String temp = "";
                        for (int i = 0; i < ficheros.length; i++) {
                            temp = temp + ficheros[i] + "\n";
                        }
                        imprime(temp);

                    }
                } else if (splits.length == 2) {
                    File dir = new File(defaultdir + "/" + splits[1]);
                    String[] ficheros = dir.list();
                    if (ficheros == null) {
                        System.out.println("Nel prra, nel");
                    } else {
                        String temp = "";
                        for (int i = 0; i < ficheros.length; i++) {
                            temp = temp + ficheros[i] + "\n";
                        }
                        imprime(temp);

                    }
                }
                break;

            case "copy":
                if (splits.length == 3) {
                    File archCopy = new File(defaultdir, splits[1]);
                    File archCopy2 = archCopy;
                    if (archCopy.exists()) {
                        archCopy.renameTo(new File(defaultdir + "/" + splits[2], splits[1]));
                        archCopy.createNewFile();
                        archCopy2.createNewFile();
                        System.out.println(archCopy.exists());
                        imprime("Copiado " + splits[2]);
                    } else {
                        JOptionPane.showMessageDialog(null, "No existe el archivo");
                    }

                    //File direc = new    
                    //File newArch = new File(splits[2], splits[1]);
                    //newArch.createNewFile();
                }
                break;
            case "move":
                if (splits.length == 3) {
                    File archCopy = new File(defaultdir, splits[1]);
                    File archCopy2 = archCopy;
                    if (archCopy.exists()) {
                        archCopy2.renameTo(new File(defaultdir + "/" + splits[2], splits[1]));
                        archCopy2.createNewFile();
                        imprime("Movido " + splits[2]);
                        if (archCopy.exists()) {
                            archCopy.delete();
                            archCopy.delete();
                            archCopy.delete();
                        }
                        System.out.println(archCopy.exists());
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "No existe el archivo");
                    }
                    
                }
                break;
            case "remove":
               File fileRemove = new File(defaultdir, splits[1]);
                if (fileRemove.exists()) {
                    fileRemove.delete();
                    imprime("Borrado " + splits[2]);
                }else{
                JOptionPane.showMessageDialog(null, "No existe el archivo");
                }
                break;
            case "cd":
                String tmp = splits[1];
                defaultdir =defaultdir +"/"+ tmp;
                System.out.println(defaultdir);
                break;
            case "rt":
                defaultdir = "/home/charles/NetBeansProjects/GestorArchivo/home";
                System.out.println(defaultdir);
        }
        jTextField1.setText("");
    }

